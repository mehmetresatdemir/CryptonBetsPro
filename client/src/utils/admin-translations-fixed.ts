export type AdminLanguage = 'tr' | 'en';

export const getAdminTranslation = (key: string, language: 'tr' | 'en' = 'tr'): string => {
  return adminTranslations[key]?.[language] || key;
};

// Alias for shorter usage
export const tr = getAdminTranslation;

// Function to get current admin language
export const getAdminLanguage = (): AdminLanguage => {
  const savedLang = localStorage.getItem('admin-language') as AdminLanguage | null;
  return savedLang && ['tr', 'en'].includes(savedLang) ? savedLang : 'tr';
};