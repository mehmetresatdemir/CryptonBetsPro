// Çeviri fonksiyonu
export const getPaymentMessage = (key: string, language: string, params?: Record<string, string | number>): string => {
  const lang = language as keyof typeof paymentTranslations;
  const messages = paymentTranslations[lang] || paymentTranslations.tr;
  let message = (messages as any)[key] || key;
  
  // Parametreleri değiştir
  if (params) {
    Object.entries(params).forEach(([param, value]) => {
      message = message.replace(`{${param}}`, value.toString());
    });
  }
  
  return message;
};