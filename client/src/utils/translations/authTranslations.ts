// Get auth message by key and language
export function getAuthMessage(key: string, language: string = 'tr'): string {
  const lang = language as keyof typeof authTranslations;
  const messages = authTranslations[lang] || authTranslations.tr;
  return (messages as any)[key] || key;
}

